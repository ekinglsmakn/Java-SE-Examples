package StringExamples;

public class ReplaceMethod {
    /*
    * Replace: Seçilen karakteri tüm stringde arar. Ve bulduğunda stringde ne kadar varsa hepsini
    * yenisiyle değiştirir.
    * */
    public static void main(String[] args) {
        String str = "My favorite movie is Lord Of The Rings";
        System.out.println(str.replace('o','S'));
    }
}
